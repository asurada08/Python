from tkinter import * # tkinter는 파이썬에서 GUI 관련 모듈 제공해주는 표준 윈도우 라이브러리

window = Tk() # Tk()는 기본이 되는 윈도우를 반환, 이를 루트 윈도우 또는 베이스 윈도우라고 함.

## 이 부분에서 화면을 구성하고 처리 ##

window.mainloop() # 3행에서 베이스 윈도우를 window변수에 넣고 함수 실행

## tkinter 는 TK Interface의 약어. Tk는 Tcl/Tk라는 전통적인 GUI 인터페이스 윈도, 리눅스, 맥 등에서 모두 동일한 코드로 사용 가능
